async function main() {
  console.log(`Mode: ${LIVE ? "LIVE" : PREVIEW ? "PREVIEW" : "DRY RUN"}`);

  const { rows: products } = await pool.query(`
    SELECT id, name
    FROM catalog_unified
    WHERE source_vendor = 'VTWIN' AND category IS NULL
  `);
  console.log(`\nVTwin products with null category: ${products.length.toLocaleString()}`);

  const assignments = [];
  const unmatched   = [];

  for (const p of products) {
    const upper = p.name.toUpperCase();
    let matched = null;
    for (const rule of RULES) {
      if (rule.keywords.some(kw => upper.includes(kw))) {
        matched = rule.category;
        break;
      }
    }
    if (matched) assignments.push({ id: p.id, name: p.name, category: matched });
    else unmatched.push(p);
  }

  const byCat = {};
  for (const a of assignments) byCat[a.category] = (byCat[a.category] ?? 0) + 1;

  console.log(`\nMatched:   ${assignments.length.toLocaleString()} (${(assignments.length / products.length * 100).toFixed(1)}%)`);
  console.log(`Unmatched: ${unmatched.length.toLocaleString()}`);
  console.log("\nCategory breakdown:");
  Object.entries(byCat).sort((a, b) => b[1] - a[1])
    .forEach(([cat, cnt]) => console.log(`  ${cnt.toString().padStart(6)}  ${cat}`));

  if (PREVIEW) {
    console.log("\n── Sample matches per category ──────────────────────────────");
    for (const [cat] of Object.entries(byCat).sort((a, b) => b[1] - a[1])) {
      const samples = assignments.filter(a => a.category === cat).slice(0, 5);
      console.log(`\n${cat}:`);
      samples.forEach(s => console.log(`  ${s.name}`));
    }
    if (unmatched.length > 0) {
      console.log("\n── Unmatched ────────────────────────────────────────────────");
      unmatched.forEach(p => console.log(`  ${p.name}`));
    }
    const misc = assignments.filter(a => a.category === "COMMON MISC GROUP");
    if (misc.length > 0) {
      console.log(`\n── All COMMON MISC GROUP items (${misc.length}) ──────────────────`);
      misc.forEach(p => console.log(`  ${p.name}`));
    }
  }

  if (!LIVE) {
    console.log("\nDry run complete. Pass --live to update catalog_unified.");
    await pool.end();
    return;
  }

  console.log("\nUpdating catalog_unified …");
  const BATCH = 500;
  let updated = 0;
  for (let i = 0; i < assignments.length; i += BATCH) {
    const batch = assignments.slice(i, i + BATCH);
    const ids   = batch.map((_, j) => `$${j * 2 + 1}`).join(", ");
    const cases = batch.map((_, j) => `WHEN id = $${j * 2 + 1} THEN $${j * 2 + 2}`).join(" ");
    const flat  = batch.flatMap(a => [a.id, a.category]);
    const res = await pool.query(
      `UPDATE catalog_unified SET category = CASE ${cases} END WHERE id IN (${ids})`, flat
    );
    updated += res.rowCount ?? 0;
    if (i % 5000 === 0) process.stdout.write(`  … ${i.toLocaleString()} / ${assignments.length.toLocaleString()}\r`);
  }
  console.log(`\n  Updated: ${updated.toLocaleString()} rows`);
  console.log(`  Still null: ${unmatched.length.toLocaleString()} rows`);
  console.log("\nDone. Reindex Typesense when ready.");
  await pool.end();
}

main().catch(err => { console.error(err); process.exit(1); });
